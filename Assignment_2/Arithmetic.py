# This is Arithmetic module file which contains Add(), Sub(), Mult(), and Div() methods to perform mathematical operation on 2 numbers

def Add(no1, no2):
    return no1 + no2

def Sub(no1, no2):
    return no1 - no2

def Mult(no1, no2):
    return no1 * no2

def Div(no1, no2):
    return no1 / no2